#include <bits/stdc++.h>
using namespace std;

vector<int> rnk, parent;
vector<pair<int, int>> ovr; // অতিরিক্ত রাস্তা (সাইকেল তৈরি করা রাস্তা)

void makeSet(int n) {
    rnk.assign(n + 1, 0);
    parent.resize(n + 1);
    for (int i = 1; i <= n; i++) {
        parent[i] = i;
    }
}

int findParent(int v) {
    if (parent[v] == v) return v;
    return parent[v] = findParent(parent[v]); // পাথ কম্প্রেশন
}

void rankUnion(int uu, int vv) {
    int u = findParent(uu);
    int v = findParent(vv);

    if (u == v) {
        // যদি রুট একই হয়, তার মানে এই রাস্তাটি সাইকেল তৈরি করছে
        ovr.push_back({uu, vv});
    } else {
        // ইউনিয়ন বাই র‍্যাঙ্ক (Union by Rank)
        if (rnk[u] < rnk[v]) swap(u, v);
        parent[v] = u;
        if (rnk[u] == rnk[v]) rnk[u]++;
    }
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int n;
    if (!(cin >> n)) return 0;

    makeSet(n);

    // n-1 টি রাস্তার ইনপুট প্রসেস করা
    for (int i = 0; i < n - 1; i++) {
        int u, v;
        cin >> u >> v;
        rankUnion(u, v);
    }

    // আলাদা আলাদা কম্পোনেন্টের রুটগুলো বের করা
    vector<int> roots;
    for (int i = 1; i <= n; i++) {
        if (parent[i] == i) {
            roots.push_back(i);
        }
    }

    // আউটপুট: কতগুলো রাস্তা বদলাতে হবে
    // যতগুলো কম্পোনেন্ট (roots.size()), তার থেকে ১টি কম রাস্তা লাগবে কানেক্ট করতে
    int t = roots.size() - 1;
    cout << t << endl;

    // অতিরিক্ত রাস্তাগুলো ব্যবহার করে রুটগুলোকে জোড়া লাগানো
    for (int i = 0; i < t; i++) {
        // ফরম্যাট: পুরনো_রাস্তা_u পুরনো_রাস্তা_v নতুন_রাস্তা_u নতুন_রাস্তা_v
        // আমরা রুট[i] এবং রুট[i+1] কে কানেক্ট করছি
        cout << ovr[i].first << " " << ovr[i].second << " " 
             << roots[i] << " " << roots[i+1] << "\n";
    }

    return 0;
}
